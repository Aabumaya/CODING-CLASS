// Declare Variables
const radius = 5; // I use const because that's the given radius of circle

const pi = 3.14; // I use const because pi is a fixed value

// I use const for area and perimeter because they are calculated once and won't change
const area = pi * radius * radius; // formula for the area of a circle

const perimeter = 2 * pi * radius; // formula for perimeter of a circle

// messsage to the console
console.log("The area of the circle is " + area);

console.log("The perimeter of the circle is " + perimeter);
